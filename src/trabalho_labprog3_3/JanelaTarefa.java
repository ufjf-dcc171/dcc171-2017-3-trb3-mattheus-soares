    private void btnAlterarTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarTarefaActionPerformed
        if (!lstTarefas.isSelectionEmpty()) {
            /*!txtDescricaoEditar.getText().isEmpty() && !txtDuracaoEditar.getText().isEmpty()
                && txtPercentualEditar.getText().isEmpty() && txtDataInicioEditar.getText().isEmpty()
                && txtDataFimEditar.getText().isEmpty()&& txtStatusEditar.getText().isEmpty() */
            try {
                idTarefaAtual = lstTarefas.getSelectedValue().getId();
                Tarefa t = new Tarefa();
                TarefaDAO dao = new TarefaDAOJDBC();

                if (!txtDescricaoEditar.getText().isEmpty()) {
                    t.setDescricao(txtDescricaoEditar.getText());
                    dao.atualizarTarefaDescricao(idTarefaAtual, t.getDescricao());
                }

                if (!txtDuracaoEditar.getText().isEmpty()) {
                    t.setDuracao_esperada(Integer.parseInt(txtDuracaoEditar.getText()));
                    dao.atualizarTarefaDuracaoEsperada(idTarefaAtual, t.getDuracao_esperada());
                }

                if (txtPercentualEditar.getText().isEmpty()) {
                    t.setPercentual(Double.parseDouble(txtPercentualEditar.getText()));
                    dao.atualizarTarefaPercentual(idTarefaAtual, t.getPercentual());

                }

                if (txtStatusEditar.getText().isEmpty()) {
                    t.setStatus(Integer.parseInt(txtStatusEditar.getText()));
                    dao.atualizarTarefaStatus(idTarefaAtual, t.getStatus());

                }

            } catch (Exception ex) {
                Logger.getLogger(JanelaTarefa.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnAlterarTarefaActionPerformed

