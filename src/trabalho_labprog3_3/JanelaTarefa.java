    private void btnCriarTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarTarefaActionPerformed
        if (!txtDescricaoTarefa.getText().isEmpty() && !txtDuracaoTarefa.getText().isEmpty()
                && !txtPercentualTarefa.getText().isEmpty() && !lstProjetos.isSelectionEmpty()) {

            try {
                idProjeto = lstProjetos.getSelectedValue().getId();
                TarefaDAO dao = new TarefaDAOJDBC();
                Tarefa t = new Tarefa();
                t.setDescricao(txtDescricaoTarefa.getText());
                t.setDuracao_esperada(Integer.parseInt(txtDuracaoTarefa.getText()));
                t.setPercentual(Double.parseDouble(txtPercentualTarefa.getText()));
                t.setID_Projeto(idProjeto);
                dao.criarTarefa(t, idProjeto);

                tarefas = dao.listarTarefasProjeto(idProjeto);

                lstTarefas.setModel(new TarefaListModel(tarefas));
            } catch (Exception ex) {
                Logger.getLogger(JanelaTarefa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCriarTarefaActionPerformed

